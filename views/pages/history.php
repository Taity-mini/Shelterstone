    <h2>Origins</h2>
    <p>
        RGU Shelterstone is one of RGU's oldest Sports Clubs, founded in 1962 as RGCT's Mountaineering Club (updating its' name along with the University when it gained its' University status in 1992). </br></p>
    <p>The name Shelterstone comes from 'The Shelterstone', a large boulder in the Cairngorms under which mountaineers have been known to shelter from from the elements.</br></p>

        Originally the club was more oriented towards outdoor trips, becoming more involved with rock climbing in 2005 when a purpose built indoor climbing facility was built as part of the new RGU SPORT building. This lead to weekly training sessions, promoting a rise in members and participation in climbing competitions throughout the UK.</br></br>

        True to our roots, and to keep ourselves modern, as well as a Club President we have two Vice Presidents, one dedicated to Climbing Competitions, the other to our outdoor trips. </br>
